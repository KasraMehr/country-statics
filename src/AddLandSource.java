    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String landType = jComboBox1.getSelectedItem().toString();
        String typeName = jTextField1.getText();
        double height = (double) jSpinner1.getValue();
        double area = (double) jSpinner2.getValue();
        int number = (int) jSpinner4.getValue();
        boolean isSpecial = jCheckBox1.isSelected();
        String specialSourceName = jTextField2.getText();
        String description = jTextArea2.getText();
        String direction = jComboBox3.getSelectedItem().toString();
        String state = jTextField3.getText();
        double distanceFromCenter = (double) jSpinner5.getValue();
        String innerType = jComboBox2.getSelectedItem().toString();
        boolean outside = jCheckBox2.isSelected();
        double humidityRate = (double) jSpinner3.getValue();
        
        ObjectFileIO fileIO = new ObjectFileIO();
        
        Location location;
        
        while(true)
        {
            if (!landType.equals("Select") & !typeName.equals(null) & !direction.equals("Select"))
            {
                location = new Location(direction , distanceFromCenter , state);
                
                if (isSpecial)
                {
                    SpecialLandSource special = new SpecialLandSource(landType , innerType , height , area , typeName , number , isSpecial , location , specialSourceName , description);
                    fileIO.WriteSpecialLandSource(special, ObjectList.landSourcesList, ObjectList.specialLandSourceList);
                    
                    this.dispose();
                    new RegisterMenu().setVisible(true);
                    
                    break;
                }
                else
                {
                    if (landType.equals("Water"))
                    {
                        if (!innerType.equals(null))
                        {
                            Water water = new Water(landType , innerType , height , area , typeName , number , isSpecial , location , outside);
                            fileIO.WriteWater(water, ObjectList.landSourcesList, ObjectList.waterList);
                            
                            this.dispose();
                            new RegisterMenu().setVisible(true);
                            
                            break;
                        }
                    }
                    else if (landType.equals("Land"))
                    {
                        if (!innerType.equals(null))
                        {
                            Land land = new Land(landType , innerType , height , area , typeName , number , isSpecial , location , humidityRate);
                            fileIO.WriteLand(land, ObjectList.landSourcesList , ObjectList.landList);
                            
                            this.dispose();
                            new RegisterMenu().setVisible(true);
                            
                            break;       
                        }
                    }
                }
            }
            
            break;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

