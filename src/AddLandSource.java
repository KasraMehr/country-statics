    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        // TODO add your handling code here:
        if (evt.getItem().equals("Select"))
        {
            jLabel6.setVisible(false);
            jLabel8.setVisible(false);
            jComboBox2.setVisible(false);
            jCheckBox2.setVisible(false);
            jSpinner3.setVisible(false);
        }
        else if (evt.getItem().equals("Land"))
        {
            jComboBox2.removeAllItems();
            jComboBox2.addItem("Select");
            jComboBox2.addItem("mountain");
            jComboBox2.addItem("jungle");
            jComboBox2.addItem("desert");
            jComboBox2.setVisible(true);
            jLabel6.setVisible(true);
            jCheckBox2.setVisible(false);
            jLabel8.setVisible(true);
            jSpinner3.setVisible(true);
        }
        else if (evt.getItem().equals("Water"))
        {
            jLabel8.setVisible(false);
            jSpinner3.setVisible(false);
            
            jComboBox2.removeAllItems();
            jComboBox2.addItem("Select");
            jComboBox2.addItem("pond");
            jComboBox2.addItem("river");
            jComboBox2.addItem("lake");
            jLabel6.setVisible(true);
            jComboBox2.setVisible(true);
            jCheckBox2.setVisible(true);
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

